import jieba
dk = {}
with open('data.txt','r',encoding='utf-8') as f:
    row_list = f.readlines()
for row in row_list:
    words = jieba.lcut(row.strip("\n"), cut_all = True)
    for word in words :
        if len(word) == 2 :
            dk[word] = dk.get(word, 0) +1

dp = list(dk.items())
dp.sort(key= lambda x:int(x[1]), reverse = True)

for i in range(10) :
    print("{}:{}".format(dp[i][0], dp[i][1]))
